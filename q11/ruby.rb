#解法　(問題の定義が曖昧だったので、解法だけ覚えてパスします)
N = 9

def vote(n)
    return 1 if n <= 2
    way = 1
    way += vote(n - 1)
    2.upto(n - 1) do |i|
        way += vote(i) * vote(n - 1)
    end
    way
end

puts vote(N)

